    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int getRow = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();

        this.selectedBrandId = jTable1.getSelectedRow();

        jTextField2.setText(model.getValueAt(getRow, 1).toString());

        jButton7.setEnabled(true);
        try {
            String selectedBrandName = model.getValueAt(getRow, 1).toString();
            ResultSet rs = MySQL.execute("SELECT `description` FROM `equipment` WHERE `equipment_name` = '" + selectedBrandName + "'");

            if (rs.next()) {
                jTextArea1.setText(rs.getString("description"));
            } else {
                jTextArea1.setText("No description available for the selected brand.");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jTable1MouseClicked


