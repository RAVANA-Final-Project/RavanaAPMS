    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        String equipmentName = jTextField2.getText();
        //String BrandName = jTextField1.getText();
        String Desctiption = jTextArea1.getText();

        int SelectRow = jTable1.getSelectedRow();

        if (equipmentName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Equipment Name ", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (equipmentName.length() > 45) {
            JOptionPane.showMessageDialog(this, "Equipment Name must be hav only 45 Text", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.setText("");
            //} else if (BrandName.isEmpty()) {
            //  JOptionPane.showMessageDialog(this, "Please Select Brand ", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (Desctiption.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Equipment Name must be hav only 45 Text", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            if (SelectRow != -1) {
                String oldEqName = jTable1.getValueAt(SelectRow, 1).toString();
                String newEqName = jTextField2.getText();

                if (newEqName != null && !newEqName.trim().isEmpty()) {
                    try {
                        MySQL.execute("UPDATE `equipment` SET `equipment_name` = '" + newEqName + "', `description` =  '" + Desctiption + "' WHERE `equipment_name` = '" + oldEqName + "'");

                        JOptionPane.showMessageDialog(this, "Equipment updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    } catch (Exception e) {
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(this, "Error updating Equipment name!", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Equipment cannot be empty!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please select a Equipment to update.", "Warning", JOptionPane.WARNING_MESSAGE);
            }
                
            jTextField2.setText("");
            jTextArea1.setText("");
                // Refresh the table
                loadTable();

        }

    }//GEN-LAST:event_jButton10ActionPerformed

