    private void loadTable() {

        try {
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

            model.setRowCount(0);
            ResultSet rs = MySQL.execute("SELECT * FROM `equipment_has_equipment_brand`"
                    + " INNER JOIN `equipment_brand` ON equipment_has_equipment_brand.`equipment_brand_equipment_brand_id` = `equipment_brand`.`equipment_brand_id`"
                    + "  WHERE `equipment_equipment_id`='" + this.eqid + "' ");

            //DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            int counter = 0;

            while (rs.next()) {
                counter++;
                Vector<String> vector = new Vector<>();
                vector.add(String.valueOf(counter));
                vector.add(rs.getString("equipment_brand_name"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            SplashScreen.logger.log(Level.WARNING, "Warning Message", e);
        }
    }

    private void loadBrand() {
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `equipment_brand`");
            Vector<String> vector = new Vector<>();
            vector.add("");

            while (rs.next()) {
                String brandName = rs.getString("equipment_brand_name");
                int brandID = rs.getInt("equipment_brand_id");
                vector.add(brandName);

                BrandMap.put(brandName, brandID);

                DefaultComboBoxModel<String> model1 = new DefaultComboBoxModel<>(vector);
                jComboBox1.setModel(model1);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private static boolean isTableNotEmpty(JTable table) {
        for (int row = 0; row < table.getRowCount(); row++) {
            for (int col = 0; col < table.getColumnCount(); col++) {
                Object value = table.getValueAt(row, col);
                if (value != null && !value.toString().trim().isEmpty()) {
                    return true;
                }
            }
        }
        return false;
    }

