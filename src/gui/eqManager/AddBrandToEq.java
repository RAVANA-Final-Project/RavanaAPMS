    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        int eq = eqid;
        int selectedRow = jTable2.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        String brandName = model.getValueAt(selectedRow, 1).toString();

        Integer brandId = BrandMap.get(brandName);
        if (brandId == null) {
            JOptionPane.showMessageDialog(this, "ERROR: Brand not found in BrandMap", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this entry?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {

            try {
                MySQL.execute("DELETE FROM `equipment_has_equipment_brand` WHERE `equipment_equipment_id` = '" + eq + "'");
            } catch (Exception e) {
                e.printStackTrace();
            }
            
            loadTable(); // Refresh the table to show the updated data
            
        }

    }//GEN-LAST:event_jButton11ActionPerformed

