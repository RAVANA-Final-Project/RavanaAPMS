    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed

        String BrandName = jTextField2.getText();

        if (BrandName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Brand Name ", "Warning", JOptionPane.WARNING_MESSAGE);

        }else if (BrandName.length() > 45){
             JOptionPane.showMessageDialog(this, "Brand Name must be hav only 45 Text", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.setText("");
        
        }  else {

            try {
                ResultSet BrandRs = MySQL.execute("SELECT * FROM `equipment_brand` WHERE `equipment_brand_name` = '" + BrandName + "' ");
                if (BrandRs.next()) {
                    JOptionPane.showMessageDialog(this, "This Brand Alrady Added !", "Warning", JOptionPane.WARNING_MESSAGE);
                    jTextField2.setText("");
                } else {
                    try {
                        MySQL.execute("INSERT INTO `equipment_brand` (`equipment_brand_name`) VALUE ('" + BrandName + "')");

                        HashMap<String, String> BrandIdMap = new HashMap<>();
                        BrandIdMap.put("equipment_brand_name", BrandName);

                        JOptionPane.showMessageDialog(this, "Successfully Added Brand !", "Brand Added", JOptionPane.INFORMATION_MESSAGE);
                        jTextField2.setText("");

                        loadBrands();
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton10ActionPerformed

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        String BrandName = jTextField2.getText();
        
        int SelectRow = jTable1.getSelectedRow();
        
        if (SelectRow != -1){
             String oldBrandName = jTable1.getValueAt(SelectRow, 1).toString(); 
             String newBrandName = jTextField2.getText();
             
         if (newBrandName != null && !newBrandName.trim().isEmpty()) {
            try {
                MySQL.execute("UPDATE `equipment_brand` SET `equipment_brand_name` = '" + newBrandName + "' WHERE `equipment_brand_name` = '" + oldBrandName + "'");

                JOptionPane.showMessageDialog(this, "Brand name updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

                // Refresh the table
                loadBrands();
                
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error updating brand name!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Brand name cannot be empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a brand to update.", "Warning", JOptionPane.WARNING_MESSAGE);
    }
        
    }//GEN-LAST:event_jButton11ActionPerformed

