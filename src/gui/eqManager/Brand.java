    private void loadBrands() {
        try {
            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.setRowCount(0);

            ResultSet BrandResultset = MySQL.execute("SELECT * FROM `equipment_brand`");

            int counter = 0;

            while (BrandResultset.next()) {
                counter++;

                Vector<String> vector = new Vector<>();
                vector.add(String.valueOf(counter));
                vector.add(BrandResultset.getString("equipment_brand_name"));

//             BrandIdMap.put(BrandResultset.getString("equipment_brand_name"), BrandResultset.getString("equipment_brand_id"));
                tableModel.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    

