    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String email = jTextField2.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        String message = "";

        if (email.isEmpty()) {
            message = "Please enter email";
        } else if (email.length() > 45) {
            message = "Email too long";
        } else if (email.length() < 3) {
            message = "Email too short";
        } else if (!email.matches("^[a-zA-Z0-9_+&*-]+(?:\\."+ 
                            "[a-zA-Z0-9_+&*-]+)*@" + 
                            "(?:[a-zA-Z0-9-]+\\.)+[a-z" + 
                            "A-Z]{2,7}$")) {
            message = "Invlied email";
        } else if (password.isEmpty()) {
            message = "Please enter password";
        } else if (password.length() > 16) {
            message = "Password too long";
        } else if (password.length() < 8) {
            message = "Password too short";
        } else if (!password.matches("^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d]{8,}$")) {
            message = "Invalied password";
        } else {
            message = "valied";
        }
        
        if (message.equals("valied") && !message.isEmpty()) {
            try {
                ResultSet resultset = MySQL.execute("SELECT * FROM `admin` INNER JOIN `admin_type` ON "
                        + "`admin`.`admin_type_admin_type_id`=`admin_type`.`admin_type_id` "
                        + "WHERE `email`='" + email + "' AND `password`='" + password + "' AND `admin_type_name`='Admin'");

                if (resultset.next()) {

                    Admin admin = new Admin();
                    admin.setEmail(resultset.getString("email"));
                    admin.setPassword(resultset.getString("password"));
                    admin.setAdminType(resultset.getString("admin_type_name"));
                    
                    AdminDashBoard adminDashBoard = new AdminDashBoard();
                    adminDashBoard.setAdminUserData(admin);
                    adminDashBoard.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Invalied username or password", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, message, "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

