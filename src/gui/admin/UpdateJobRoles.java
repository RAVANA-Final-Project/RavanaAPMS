    private void loadJobStatus() {
        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `job_position_status`");
            Vector<String> jobStatusVector = new Vector<>();
            jobStatusVector.add("Select");

            while (resultset.next()) {
                jobStatusVector.add(resultset.getString("job_position_status_name"));
                jobStatusTypesMap.put(resultset.getString("job_position_status_name"), resultset.getString("job_position_status_id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(jobStatusVector);
            jComboBox1.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadJobData() {
        try {
            ResultSet jobRolesResultset = MySQL.execute("SELECT * FROM `position` INNER JOIN `job_position_status` ON "
                    + "`position`.`job_position_status_job_position_status_id`=`job_position_status`.`job_position_status_id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (jobRolesResultset.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(jobRolesResultset.getString("position_id"));
                vector.add(jobRolesResultset.getString("position_name"));
                vector.add(jobRolesResultset.getString("job_position_status_name"));

                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

