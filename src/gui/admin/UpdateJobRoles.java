    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Update Employees
        int row = jTable1.getSelectedRow();
        String Id = String.valueOf(jTable1.getValueAt(row, 0));
        String Jobname = jTextField5.getText();
        String Jobstatus = String.valueOf(jComboBox1.getSelectedItem());
        String message;

        if (Jobname.isEmpty()) {
            message = "Type Job name";
        } else if (Jobstatus.equals("Select")) {
            message = "Select job status";
        } else {
            message = null;
        }

        if (message != null) {
            JOptionPane.showMessageDialog(this, message, "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet resultset = MySQL.execute("SELECT * FROM `position` WHERE `position_name`='" + Jobname + "' AND `job_position_status_job_position_status_id`='" + jobStatusTypesMap.get(Jobstatus) + "'");
                boolean jobUpdate = false;

                if (resultset.next()) {
                    jobUpdate = false;
                    JOptionPane.showMessageDialog(this, "This job is already registered", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    jobUpdate = true;
                }
                if (jobUpdate == true) {
                    try {
                        MySQL.execute("UPDATE `position` SET `position_name`='" + Jobname + "',`job_position_status_job_position_status_id`='" + jobStatusTypesMap.get(Jobstatus) + "'"
                                + "WHERE `position_id`='" + Id + "'");

                        loadJobData();
                        resetAll();
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed


