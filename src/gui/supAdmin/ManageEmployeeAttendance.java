    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int arrangeAttendance = JOptionPane.showConfirmDialog(this, "Are you sure want to Arrange Attendance Table?", "Arrange Attendance", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (arrangeAttendance == JOptionPane.YES_OPTION) {

            try {
                ResultSet searchDateResultset = MySQL.execute("SELECT * FROM `attendance_date` WHERE `attendance_date`='" + new SimpleDateFormat("yyy-MM-dd").format(new Date()) + "'");
                if (searchDateResultset.next()) {
                    JOptionPane.showMessageDialog(this, "Already Arranged Attendance Report for Today", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    String attendanceId = String.valueOf(System.currentTimeMillis());

                    MySQL.execute("INSERT INTO `attendance_date`(`attendance_date_id`,`attendance_date`) VALUES('" + attendanceId + "','" + new SimpleDateFormat("yyy-MM-dd").format(new Date()) + "')");
                    ResultSet seacrhEmployeeResultset = MySQL.execute("SELECT `employee_nic` FROM `employee` WHERE `employee_status_employee_status_id`='1'");

                    while (seacrhEmployeeResultset.next()) {

                        MySQL.execute("INSERT INTO `employee_attendance`(`in_time`,`out_time`,`employee_employee_nic`,`attendance_date_attendance_date_id`,`employee_attendance_status_employee_attendance_status_id`) "
                                + "VALUES('','','" + seacrhEmployeeResultset.getString("employee_nic") + "','" + attendanceId + "','3')");
                    }
                    JOptionPane.showMessageDialog(this, "Attendance Report Arranged", "Success", JOptionPane.INFORMATION_MESSAGE);

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int arrangeAttendance = JOptionPane.showConfirmDialog(this, "Do You Want to Mark Attendance?", "Mark Attendance", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (arrangeAttendance == JOptionPane.YES_OPTION) {
            if (jTextField2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "First Enter The NIC Number", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            if ((!jTextField2.getText().matches("^(?:19|20)?\\d{2}[0-9]{10}|[0-9]{9}[x|X|v|V]$"))) {
                textFieldcomponentRearrange(jTextField2);

            } else {
                try {
                    ResultSet searchAttendanceDateResultset = MySQL.execute("SELECT `attendance_date_id` FROM `attendance_date` WHERE `attendance_date`='" + new SimpleDateFormat("yyy-MM-dd").format(new Date()) + "'");

                    if (searchAttendanceDateResultset.next()) {
                        ResultSet searchEmployeeResultSet = MySQL.execute("SELECT `employee_employee_nic` FROM `employee_attendance` WHERE `employee_employee_nic`='" + jTextField2.getText() + "' AND `attendance_date_attendance_date_id`='" + searchAttendanceDateResultset.getString("attendance_date_id") + "' AND `employee_attendance_status_employee_attendance_status_id`='3'");

                        if (searchEmployeeResultSet.next()) {
                            
                            MySQL.execute("UPDATE `employee_attendance` "
                                    + "SET `in_time` = '" + new SimpleDateFormat("hh:MM:ss").format(new Date()) + "',"
                                    + "`employee_attendance_status_employee_attendance_status_id`='1' "
                                    + "WHERE `employee_employee_nic`='" + searchEmployeeResultSet.getString("employee_employee_nic") + "' AND `attendance_date_attendance_date_id`='" + searchAttendanceDateResultset.getLong("attendance_date_id") + "'");
                            JOptionPane.showMessageDialog(this, "Attendance Marked", "Succes", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(this, "No Such a Active Record In the System", "Warming", JOptionPane.WARNING_MESSAGE);
                        }

                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }

        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:

        String nic = jTextField2.getText();

        try {

            ResultSet employeeDetailsResultset = MySQL.execute("SELECT * FROM `employee` INNER JOIN `position` ON \n"
                    + "`employee`.`position_position_id`=`position`.`position_id` \n"
                    + "WHERE `employee_nic`='" + nic + "'");

            if (employeeDetailsResultset.next()) {
                String fname = employeeDetailsResultset.getString("first_name");
                String lname = employeeDetailsResultset.getString("last_name");
                String mobile = employeeDetailsResultset.getString("employee_mobile");
                String email = employeeDetailsResultset.getString("email");
                String joinedDate = employeeDetailsResultset.getString("join_date");
                String jobPosition = employeeDetailsResultset.getString("position_name");

                jLabel21.setText(fname + " " + lname);
                jLabel22.setText(mobile);
                jLabel12.setText(email);
                jLabel24.setText(joinedDate);
                jLabel23.setText(jobPosition);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jTextField2KeyReleased

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int arrangeAttendance = JOptionPane.showConfirmDialog(this, "Do You Want to Set the Leave Time", "Set the Leave Time", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (arrangeAttendance == JOptionPane.YES_OPTION) {
            try {
                ResultSet searchAttendanceDateResultset = MySQL.execute("SELECT `attendance_date_id` FROM `attendance_date` WHERE `attendance_date`='" + new SimpleDateFormat("yyy-MM-dd").format(new Date()) + "'");

                if (searchAttendanceDateResultset.next()) {

                    ResultSet availableEmployeeAttendanceResultset = MySQL.execute("SELECT * FROM `employee_attendance` WHERE `attendance_date_attendance_date_id`='" + searchAttendanceDateResultset.getLong("attendance_date_id") + "'");
                    while (availableEmployeeAttendanceResultset.next()) {

                        String query = "";
                        if (availableEmployeeAttendanceResultset.getString("employee_attendance_status_employee_attendance_status_id").equals("3")) {
                            query = "UPDATE `employee_attendance` SET `employee_attendance_status_employee_attendance_status_id`='2' WHERE `employee_employee_nic`='" + availableEmployeeAttendanceResultset.getString("employee_employee_nic") + "' AND `attendance_date_attendance_date_id`='" + searchAttendanceDateResultset.getLong("attendance_date_id") + "'";

                        } else if (availableEmployeeAttendanceResultset.getString("employee_attendance_status_employee_attendance_status_id").equals("1")) {

                            query = "UPDATE `employee_attendance` SET `out_time` = '" + new SimpleDateFormat("hh:MM:ss").format(new Date()) + "' WHERE `employee_employee_nic`='" + availableEmployeeAttendanceResultset.getString("employee_employee_nic") + "' AND `attendance_date_attendance_date_id`='" + searchAttendanceDateResultset.getLong("attendance_date_id") + "'";

                        }
                        if (!query.isEmpty()) {
                            try {
                                MySQL.execute(query);
                            } catch (Exception e) {
                                e.printStackTrace();
                            }
                        }
                    }

                    JOptionPane.showMessageDialog(this, "All Attendance Records are Finalyzed", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed


