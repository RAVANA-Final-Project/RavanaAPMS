    public void loadEmployeeAttendanceDetails() {
        try {

            ResultSet employeeAttendanceCountResultset = MySQL.execute("SELECT COUNT(`employee_employee_nic`) FROM `employee_attendance` INNER JOIN `attendance_date` ON "
                    + "`employee_attendance`.`attendance_date_attendance_date_id`=`attendance_date`.`attendance_date_id` INNER JOIN `employee_attendance_status` ON "
                    + "`employee_attendance`.`employee_attendance_status_employee_attendance_status_id`=`employee_attendance_status`.`employee_attendance_status_id` "
                    + "WHERE `attendance_date` ='" + new SimpleDateFormat("yyyy-MM-dd").format(new Date()) + "' AND `employee_attendance_status_name`='Present'");

            ResultSet totalActiveEmployeeCountResultset = MySQL.execute("SELECT COUNT(`employee_nic`) From `employee` INNER JOIN `employee_status` ON "
                    + "`employee`.`employee_status_employee_status_id`=`employee_status`.`employee_status_id` "
                    + "WHERE `employee_status_name`='Active'");

            
            if (totalActiveEmployeeCountResultset.next()) {
                jLabel18.setText(totalActiveEmployeeCountResultset.getString("COUNT(`employee_nic`)"));
            } else {
                jLabel18.setText("Still Checking......");
            }
            if (employeeAttendanceCountResultset.next()) {
                jLabel25.setText(employeeAttendanceCountResultset.getString("COUNT(`employee_employee_nic`)"));
            } else {
                jLabel25.setText("0");
            }
            

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

